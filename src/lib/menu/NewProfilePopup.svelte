<script lang="ts">
	import Popup from '../components/Popup.svelte';
	import { refreshProfiles } from '$lib/stores';
	import { invokeCommand } from '$lib/invoke';
	import BigButton from '$lib/components/BigButton.svelte';
	import InputField from '$lib/components/InputField.svelte';

	export let open = false;

	let name = '';

	async function createProfile() {
		if (name.length === 0) return;

		await invokeCommand('create_profile', { name });
		refreshProfiles();
		name = '';
		open = false;
	}
</script>

<Popup title="New profile" bind:open>
	<div class="h-1" />
	<InputField
		bind:value={name}
		placeholder="Enter profile name..."
		size="lg"
		on:submit={createProfile}
	/>
	<div class="flex w-full justify-end mt-1">
		<BigButton disabled={name.length === 0} on:click={createProfile}>Create</BigButton>
	</div>
</Popup>
